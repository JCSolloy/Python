//Cadenas de caracteres

//Las cadenas de caracteres son un tipo de dato que permite almacenar una secuencia de caracteres. En C++ se pueden representar mediante el tipo de dato string, que es una clase de la biblioteca estándar.

//Para poder utilizar el tipo de dato string, es necesario incluir la cabecera string. A continuación se muestra un ejemplo de cómo se puede utilizar este tipo de dato:

//C++

#include <iostream>
#include <string>
using namespace std;

int main() {
    string cadena = "Hola mundo";
    cout << cadena << endl;

    return 0;
}
