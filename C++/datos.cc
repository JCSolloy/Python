// Tipos de datos basicos

#include <iostream>

using namespace std;

int main(){
    int a = 10;
    float b = 3.14;
    double c = 3.1416;
    char d = 'a';
    string e = "Hola";
    bool f = true;

    cout << "Entero: " << a << endl;
    cout << "Flotante: " << b << endl;
    cout << "Double: " << c << endl;
    cout << "Char: " << d << endl;
    cout << "String: " << e << endl;
    cout << "Booleano: " << f << endl;

    return 0;
}